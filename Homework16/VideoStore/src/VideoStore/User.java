package VideoStore;

public class User {
	private int id;
	private String fullName;
	private int filmsCounter;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public int getFilmsCounter() {
		return filmsCounter;
	}

	public void setFilmsCounter(int filmsCounter) {
		this.filmsCounter = filmsCounter;
	}

}
